package Array.problems.ManTowerOrBoxStacking;

import java.util.Comparator;

public class Man {
	
	private int height;
	private int weight;
	
	public Man(int height, int weight) {
		this.height = height;
		this.weight = weight;
	}
	public int getHeight() {
		return height;
	}
	public void setHeight(int height) {
		this.height = height;
	}
	public int getWeight() {
		return weight;
	}
	public void setWeight(int weight) {
		this.weight = weight;
	}
	

}
